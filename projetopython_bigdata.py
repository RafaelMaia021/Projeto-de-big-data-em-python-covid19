def formatar_tabela(df, top=8):
    df = df.copy()
    df['Antecedente'] = df['antecedents'].apply(lambda x: ' + '.join(sorted(x)))
    df['Consequente'] = df['consequents'].apply(lambda x: ' + '.join(sorted(x)))
