# ── BLOCO 5: EXTRAÇÃO DE REGRAS DE ASSOCIAÇÃO ─────────────────
# min_threshold=0.6 → só regras com confiança ≥ 60%

def gerar_regras(freq_items, min_conf=0.6):
    try:
        # mlxtend >= 0.21 exige num_itemsets
        return association_rules(freq_items, metric='confidence',
                                 min_threshold=min_conf,
                                 num_itemsets=len(freq_items))
    except TypeError:
        return association_rules(freq_items, metric='confidence',
                                 min_threshold=min_conf)

