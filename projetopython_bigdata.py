# ═══════════════════════════════════════════════════════════
#  CONCEITOS — o que cada métrica significa:
#
#  SUPORTE    = frequência do itemset no total de transações
#               Suporte(A) = n(A) / n_total
#               Ex: 0.33 → item aparece em 33% das transações
#
#  CONFIANÇA  = probabilidade condicional: P(B | A)
#               Conf(A→B) = Suporte(A∪B) / Suporte(A)
#               Ex: 1.0 → sempre que A ocorre, B também ocorre
#
#  LIFT       = mede se A e B ocorrem juntos mais do que ao acaso
#               Lift(A→B) = Conf(A→B) / Suporte(B)
#               Lift > 1 → associação positiva (não aleatória)
#               Lift = 1 → independência
#               Lift < 1 → associação negativa
# ═══════════════════════════════════════════════════════════

